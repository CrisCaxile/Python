# Quando falamos sobre erros e exceções, algumas fontes de informação
# Consideram que no processo de depuração, podemos encontrar três tipos
# De erro: erro de sintaxe, erro de tempo de execução e erro semântico.
# Defina o conceito de cada um deles

# a) Erro de sintaxe
# b) Erro de tempo de execução
# c) Erro semântico

# Erro de sintaxe são erros na sintaxe do código, ou seja, são falhas na escrita da linguagem, valores e conteúdos que
# devem ser digitados e incluídos no programa e estão ausentes na hora da construção do código. Por exemplo os dois pontos
# no final de um def ou um if e else.

# Erros de tempo de execução já são erros específicos dentro da linguagem, como erros de tipos, erros de divisão por zero,
# erros de nome, esse tipo de erro é comum assim como o erro de sintaxe, porém acontecem quando não é respeitado as regras
# do conteúdo dentro da linguagem ao qual se está utilizando

# Erros semânticos não são exatamente 'erros' como os destacados anteriormente, na verdade, este tipo de falha está associado
# ao que o programador deseja que a linguagem faça e esta não faz da forma que ele deseja e não resulta em nenhum erro para o
# IDL em questão.